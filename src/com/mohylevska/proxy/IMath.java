public interface IMath {
    /**
     * Adding two parametrs
     * @param x first parametr
     * @param y second
     * @return sum of x and y
     */
    public double add(double x, double y);

    /**
     * Divide two parametrs
     * @param x first parametr
     * @param y second
     * @return divide x on y
     */
    public double div(double x, double y);
}
